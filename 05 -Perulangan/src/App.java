public class App  {
    public static void main(String[] args) throws Exception {

        // perulangan for
        for (int i = 0; i < 5; i++) {
            System.out.println(" ke-" + i);

        // perulangan while
        int j = 0;
        while (j < 5) {
            System.out.println("ke-" + j);
            j++;
            }

        // perulangan do-while
        int k = 0;
        do {
            System.out.println("ke-" + k);
            k++;
        } while (k < 5);

        }

    }
}